      <h1 class="titre_rh1">Règles du jeu et règlement</h1>


        <h2 class="titre_r">L'équipe</h2>
        <p class="para_r">Une équipe se compose de 13 joueurs dont 6 remplaçants. Ceux-ci peuvent entrer en jeu à tout moment à partir de leur propre zone d'exclusion (au-delà de la ligne de but), à partir du moment où l'équipe est en possession de la balle. Un gardien de but et 6 joueurs de champ évoluent dans l'eau en même temps, pour chaque équipe. Le gardien porte, généralement, le bonnet numéroté 1, de couleur rouge. Les autres joueurs portent des bonnets numérotés de 2 à 13. L'équipe qui joue à domicile porte les bonnets clairs (blancs), les visiteurs, les bonnets foncés (noirs ou bleus). Les joueurs n'ont en aucun cas le droit de mettre une combinaison (faites pour la natation et qui réduit les prises au corps) et doivent porter obligatoirement deux maillots.</p>

        <h2 class="titre_r">Le déroulement du match </h2>
        <p class="para_r">Un match officiel se décompose en 4 périodes de 8 minutes chacune de jeu effectif (périodes de 7 minutes dans le cas de match de moins de 18 ans). Ces périodes sont séparées par des temps de repos de 2 minutes sauf entre la 2ème et la 3ème période où le repos est de 5 minutes. Chaque équipe peut demander 2 temps morts d'une minute dans le match, (+1 en cas de prolongation) lorsqu'elle est en possession de la balle (attaque).</p>

        <h2 class="titre_r">Les joueurs</h2>
        <p class="para_r">Les 6 joueurs de champ s'organisent habituellement en un demi-cercle, à 5-7 mètres du but adverse, avec un joueur au centre (appelé pointe). Ce poste est assez particulier car le joueur fait face à ses coéquipiers, et tourne donc le dos au gardien adverse. Son rôle est de récupérer une passe, souvent faite sur l'eau, à tourner son défenseur (l'arrière pointe ou contre pointe) pour se retrouver face au gardien et à tirer rapidement. Le jeu pointe/arrière pointe est très engagé et très physique puisqu'une certaine tolérance est laissée aux deux joueurs. L'arbitrage est assez difficile, puisqu'on ne voit que très peu ce qui se passe sous l'eau : il n'est pas rare de voir disparaître les deux joueurs dans une eau écumante.</p>

        <h2 class="titre_r">La règle des 30 secondes</h2>
        <p class="para_r">Chaque équipe, dès qu'elle prend possession de la balle (interception, engagement, etc) dispose d'au plus 30 secondes pour tirer au but. Dans ce cas (qu'il y ait but ou non, ou encore corner), le décompte de ces 30 secondes est réinitialisé à 20 secondes. Il est également réinitialisé si un joueur de l'autre équipe est exclu. Si le décompte arrive à son terme, une faute est sifflée et la balle est donnée à l'autre équipe.</p>

        <h2 class="titre_r">Les fautes</h2>
        <p class="para_r">Le droit de charge est autorisé sur le porteur de balle. Pratiquement, une grande liberté de manœuvre est laissée aux défenseurs pour agir sur le porteur de la balle, à l'exclusion des coups visant la tête. Il faut bien savoir que les arbitres (2 situés sur les bords du bassin) n'ont qu'une vision très limitée de ce qui se passe sous l'eau.</p>

        <h2 class="titre_r">Les différentes fautes</h2>
        <ul class="para_r">
          <li>Faute simple. La balle doit être maniée à une seule main (sauf pour le gardien). Elle ne doit pas être sous l'eau. Le joueur ne peut s'appuyer sur aucune partie du bassin (buts, bords ou fond du bassin) pendant le temps de jeu (si un gardien s'appuie sur le bord du bassin, un penalty est sifflé contre son équipe). Il est également interdit de frapper le ballon avec le poing, et de lancer de l'eau sur un adversaire.</li>
          <li>Fautes graves. Une faute commise sur un attaquant dans la zone des 5 mètres est sanctionnée par un penalty, si ce dernier est en position de marquer, tiré sur la ligne des 5 mètres.</li>
          <li>Nager sur le dos de son adversaire, frapper son adversaire involontairement entraîne une faute grave avec exclusion de 20 secondes (à la troisième faute grave, le joueur est exclu du match définitivement et est remplacé).</li>
          <li>Certaines fautes graves (par exemple la perturbation volontaire du jeu) sont sanctionnées par une Exclusion Définitive Avec remplacement (EDA), si la faute est plus grave (par exemple, un coup volontaire), le joueur est exclu du match et ne peut pas être remplacé par un membre de son équipe que 4 minutes après la faute, c'est alors une Exclusion Définitive Avec remplacement + 4 minutes + Pénalty (EDA+4+P).</li>
        </ul>

        <h2 class="titre_r">L'aire de jeu</h2>
        <p class="para_r">Surface : 30 mètres par 20 mètres pour les matchs masculins (si le bassin est assez grand sinon possibilité de jouer en 25m) et 25 mètres par 20 mètres pour les matchs féminins.</p>
        <p class="para_r">Plusieurs lignes symbolisées par des plots, matérialisent la surface de jeu.</p>
        <ul class="para_r">
          <li>Une ligne rouge (2 mètres) : zone de hors jeu. Un attaquant ne peut pas y pénétrer, sauf si la balle est déjà dans cette zone ou que la passe est déjà effectuée (balle aérienne).</li>
          <li>Une ligne jaune (5 mètres) : zone de penalty, et à l'intérieur de laquelle on ne peut pas tirer de coup franc direct.</li>
          <li>Une ligne blanche : milieu du terrain, et ligne de but dans l'alignement des poteaux.</li>
        </ul>
